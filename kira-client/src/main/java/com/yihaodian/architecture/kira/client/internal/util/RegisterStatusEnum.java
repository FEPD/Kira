package com.yihaodian.architecture.kira.client.internal.util;

public enum RegisterStatusEnum {
  UNREGISTERED(1, "未注册"),
  REGISTERING(2, "正在注册"),
  REGISTER_FAILED(3, "注册失败"),
  REGISTER_SUCCESS(4, "注册成功");

  private int code;
  private String name;

  RegisterStatusEnum(int code, String name) {
    this.code = code;
    this.name = name;
  }

  public int getCode() {
    return code;
  }

  public void setCode(int code) {
    this.code = code;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
